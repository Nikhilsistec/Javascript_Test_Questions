// String Interpolation:

// String interpolation is a method of constructing strings by embedding expressions or variables inside a string literal. The values of the expressions or variables are evaluated and inserted into the string, creating a single, coherent string.

//Example
const name = 'John';
const age = 30;
const message = `Hello, my name is ${name} and I am ${age} years old.`;
console.log(message); // Output: Hello, my name is John and I am 30 years old.
// String Concatenation:

// String concatenation is a method of joining multiple strings together to create a new string. This can be done using string concatenation operators or functions, depending on the programming language.
const firstName = 'John';
const lastName = 'Doe';
const fullName = firstName + ' ' + lastName;
console.log(fullName); // Output: John Doe


